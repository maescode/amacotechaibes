							<h2>MYLIFEMYCAREER</h2>
							<h3>BECOME A CAREER OWNER</h3>
							<h4>Empowering students and Youths across Africa and Beyond</h4>
							<p>
								Become a Career Owner is a foundational and educational career empowerment and campaign program designed for three categories of youths and students: those that have chosen their career rightly but need career management skills, those that have chosen wrongly and those that are yet to choose their career path. The objective of the program is to position young people to discern their purpose and therefore, channel their passion towards their purpose. Put simply, to help young people to be passionate about their purpose. The program, therefore, empowers youths and students via campaign seminars with the necessary tools to answer these questions and create a link between them: WHO AM I? And WHAT IS MY CAREER PATH? 
							</p>
							<p>
							Through FREE career campaign seminars, these three categories are equipped with knowledge and career management skills that would position them to solve the problem of purpose mismatch (this is when there is a discrepancy between one’s passion and purpose, i.e being passionate about a wrong purpose), career mismatch (this is when there is a discrepancy between one’s career and personality), job mismatch (this is when there is a discrepancy between one’s work environment and personality), and skills mismatch (this is when there is a discrepancy between one’s skills and the expected or required skills in one’s job or career); thereby, becoming CAREER OWNERS.
							</p>
							<p>The seminars afford participants the opportunity of meeting and interacting with career professionals as well as sharing their personal experiences with one another. 
                                So far, more than 50,000 youths and students have benefited from our program. Testimonies from parents, schools, socio-cultural groups and individuals are great signs of positive and productive impacts in the lives of the beneficiaries. In addition, the program has produced three career books that have been written to meet the needs of those in the Arts, Sciences and Social Sciences. These resource materials serve as professional guides to youths and students and are sold to them at an affordable price. However, we are passionate, hungry and desirous to do more.
                             </p>
                             <p>Our ultimate goal is to partner with parents, guidance counselors, schools and all relevant stakeholders and organizations to position every youths and students in both rural and urban areas irrespective of theirs social, cultural, religious or economic status to become CAREER OWNERS.                        </p>
                             <p>Our program is unique because it is all-inclusive; targeting pre-college students, college students, post-college students, young adults and working youths who have chosen their career paths rightly but need career management skills, who have chosen wrongly and who have not chosen at all. The seminars are designed in a way that meets specific needs of each category. Through an interactive session, each category is identified and grouped for easy of control.                                     </p>
							 <p>The program is designed into two phases. The first phase comprises of the FREE career campaign seminars and the second phase is tagged 'follow-up'. The latter was necessitated by the need to ensure monitoring and results. This phase is practically done on our social media group as well as a visit call at various locations.                                   </p>
							 <h3>About Us</h3>
							 <p>Every creature was purposefully created. And it is the ultimate desire of the creator that every creature fulfills the essence of its being. Fulfilling the essence of one’s being is fundamentally tied to one’s career path. Hence, it is imperative that everyone discerns and finds his or her career path and lives it according to the rubrics, spirit and nature of such career path. While some, if not very few, can discern to find their career path; many are left confused in their wrong career path because of lack of necessary and sufficient guidance, skills and knowledge.  Worst still, in this age and time, bedeviled with so many conflicting values, our young ones are greatly affected, often asking: who am I? And what do I want to do with my life? This is where WEandHUMANITY comes in to contribute its quota in doing something meaningful to empower and equip them with the necessary and sufficient guidance, skills and knowledge to reposition them to answer these questions and become career owners.                               </p>
